/**
 * Get tips for a specific week
 */
export const getTipsForWeek = (week: number): string[] => {
  const weekTips = weeklyTips.find(t => t.week === week);
  return weekTips?.tips || ["Enjoy this special time in your pregnancy journey"];
};

